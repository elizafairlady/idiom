}

acc=0
i=0
while (( i < 100000 )); do
  a=$(( i % 8 ))
  b=$(( (i / 8) % 8 ))
  classify "$a" "$b" "$i"
  acc=$(( (acc + v) % 1000000007 ))
  i=$(( i + 1 ))
done
printf '%s\n' "$acc"
